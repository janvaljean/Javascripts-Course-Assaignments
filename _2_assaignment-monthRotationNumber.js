//! which gives a sequence number corresponding to the entered monthName ---- switch case

let monthName = prompt(" months = January February, March, April, May, June, July, August, September, October, November, December, Enter a month name:   ");
switch (monthName) {
    case "January":
    console.log("month number 1");    
    break;
    case "February":
    console.log("month number 2");    
    break;
    case "March":
    console.log("month number 3");    
    break;
    case "April":
    console.log("month number 4");    
    break;
    case "May":
    console.log("month number 5");    
    break;
    case "June":
    console.log("month number 6");    
    break;
    case "July":
    console.log("month number 7");    
    break;
    case "August":
    console.log("month number 8");    
    break;
    case "September":
    console.log("month number 9");    
    break;
    case "October":
    console.log("month number 10");    
    break;
    case "November":
    console.log("month number 11");    
    break;
    case "December":
    console.log("month number 12");    
    break;
    default:
    alert(`Sorry, you entered invalid name`);
}


